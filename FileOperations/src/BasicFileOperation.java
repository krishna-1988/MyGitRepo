public class BasicFileOperation {
    int a;
    int b;
    int sum(int a,int b) {
        this.a = a;
        this.b = b;
        int sumValue = this.a+this.b;
        return sumValue;
    }
    int sub(int a,int b){
        this.a = a;
        this.b = b;
        int subValue = this.a - this.b;
        return  subValue;
    }
}
